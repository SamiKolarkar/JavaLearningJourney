package Loops;

public class ForWhileDoWhile {
    public static void main(String[] args) {
        for (int i=11;i<=10;i++) {
            System.out.println(i);
        }
        int i=11;
        while (i<=10) {
            System.out.println(i);
            i++;
        }
        do {
            System.out.println(i);
            i++;
        } while (i<=10);
    }
}
